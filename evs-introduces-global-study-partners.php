                        <h1 class="title">EVS introduces Global Study Partners facility to students who wish to study abroad</h1>

                        <p class="first-p">
                        Leading Visa consultancy agency Expo Visa Services (EVS), recently introduced the highly individualized, innovative, scalable student recruitment platform Global Study Partners (GSP) which provides guidance from application to enrolment to prospective students and those wishing to migrate and study abroad. EVS is the first and exclusive travel and visa partner of GSP in Sri Lanka. 
                        </p>

                        <p>A free service, GSP offers a unique online two-sided EdTech marketplace, linking millions of students through their agents and channels to over 35,000 courses and 500 institutions globally. The gamut of courses accessible through GSP vary from Pathway Programs to Vocational Training, Private Higher Education and University. </p>
                        
                        <p>" While the global pandemic has dampened hopes of prospective students worldwide, we here at EVS have been working to help students surpass their preliminary doubts and fears into expanding their educational aspirations when choosing to pursue their higher and vocational education. The GSP facility helps students understand and assess the imperative decisions when choosing their place of study, preferred educational course, price range of the select mode of education, if the course is suitable to their career prospects and other key questions such as if they can speak to someone locally in person or even compare courses.</p>

                        <p>The uniqueness of Global Study Partners is that it caters to transforms lives through education by providing access to expertise, platforms and services needed to empower students to study anywhere in the world. Such services are developed to put students at ease in a VUCA world such as ours. We encourage students to try out this free service and not to lose hope in fulfilling their vocational, educational and ultimately career prospects," stated Chanaka Rajaratne, Head of EVS. </p>

                        <p>Through GSP students may purchase additional products prior to their departure to their educational destination. These products include but are not limited to Compulsory Student Health Cover, Study Loans, Accommodation, Wi-Fi & Connectivity, Travel and even extra tutoring. GSP is not only limited to the student’s educational prospects but also provides students with access to over 900 recruitment partners across the globe.</p>

                        <p>Due to a high-cost factor when studying overseas, GSP offers Scholarships in Australia, Tasmania, Ireland, UK, USA, Canada & Singapore. Students who may wish to utilize the GSP facility can now reach out to EVS and get in touch with any trusted expert advisor who are available to deliver highly qualified counselling & provide insights into the world’s best study options. </p>

                        <p><strong></strong>Expo Visa Services operates as a fully-fledged Visa Consultancy provider. A subsidiary of the Leisure Arm of Expolanka Holdings PLC, the company facilitates the entire visa application process from filling applications, documentation, collecting and delivering documents, embassy submission and accompanying applicants to their visa submissions where necessary. The company also provides other services such as attestation, translations and more importantly door to door services. With a proven record of success, EVS aims to ensure the highest convenience and accuracy throughout their wide portfolio of solutions.</strong></p>


