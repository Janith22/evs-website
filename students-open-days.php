    $(document).ready(function() {
        $('#student_inquiry_page').val("Students Open Days");
        $('#page_name').val("Students Open Days");
        bgimgset()

        function bgimgset() {
            var scrollTop = $('#main-top').offset().top,
                elementOffset = $('#main-img1').offset().top,
                elementheight = $('#main-img1').outerHeight(),
                distance = ((elementOffset - scrollTop) + elementheight);
            var ha = 'calc(' + distance + 'px - 15vh)';
            $('#bg-div').css('height', ha)
        }
        $(window).on('resize', function() {
            bgimgset()
        });
    });
    $(document).ready(function() {
        $('.m-slider .slide>img').fadeOut();
        $('.modal').on('shown.bs.modal', function(e) {
            $('.m-slider').slick({
                slidesToShow: 1,
                slidesToScroll: 1,
                arrows: false,
                fade: true,
                dots: true
            });
            $('.wrap-modal-slider').addClass('open');
            $('.m-slider .slide>img').fadeIn();
        });
        $('.modal').on('hidden.bs.modal', function(e) {
            $('.m-slider').slick('unslick');
            $('.modal-nav-slider').slick('unslick');
            $('.wrap-modal-slider').removeClass('open');
            $('.m-slider .slide>img').fadeOut();
        });
    });
