                        <h1 class="title">6 Best Countries to Migrate for Sri Lankans</h1>

                        <p class="first-p">
                            We all dream of starting a new life in a new land. Not all may see that dream come true, but
                            if you have the right pathway stretched in front of you, the chances are much likely.
                        </p>

                        <p>Migration is not simple. It’s a total lifestyle change where you will switch careers, arrive
                            in a land of new culture and language and if you are married and with a family, you should
                            always be keen on your decision. </p>

                        <p>We put up a list of 6 best countries to Migrate for Sri Lankan citizens. Let’s take a read.
                        </p>

                        <h4 class="mb-4"><b>1. Canada </b></h4>

                        <img src="images/students-blog/canada.jpg" alt="" class="middle-img">

                        <p><a href="https://etacanada.co.uk/what-is-the-cost-of-living-in-canada-vs-the-uk/"
                                target="_blank">https://etacanada.co.uk/what-is-the-cost-of-living-in-canada-vs-the-uk/</a>
                        <p>Canada topped our list for the best place to migrate for Sri Lankans in 2021. Home to 37
                            million people, Canada is the second largest country in the world by size. They have a
                            fairly smooth governance and economy, a <a href="https://www.evs.lk/" target="_blank">number
                                of opportunities for skilled workers</a>, and a safe place for immigrants.</p>
                        <p>Although Canada is a Western Country, you can find quite a number of Sri Lankan communities
                            settled there already, so fitting in would be the least of your worries.
                        </p>

                        <h4 class="mb-4"><b>2. Norway </b></h4>

                        <img src="images/students-blog/norway.jpg" alt="" class="middle-img">

                        <p><a href="https://www.touropia.com/best-places-to-visit-in-norway/"
                                target="_blank">https://www.touropia.com/best-places-to-visit-in-norway/</a>
                        <p>Norway has a population of around 5 million people and is known as the happiest country in
                            the world! Who wouldn’t want to migrate here?? Norway offers many opportunities for IT,
                            construction and healthcare personnel. Additionally, Living standards are quite admirable.
                        </p>
                        <p>However applying for Norwegian migration can get a little complex, but with the right
                            qualifications and documentation, you’d be able to get it granted.
                        </p>

                        <h4 class="mb-4"><b>3. Malaysia </b></h4>

                        <img src="images/students-blog/malaysia.jpg" alt="" class="middle-img">

                        <p><a href="https://www.nst.com.my/opinion/letters/2018/01/326638/foreigner-i-can-say-malaysia-great-place-live"
                                target="_blank">https://www.nst.com.my/opinion/letters/2018/01/326638/foreigner-i-can-say-malaysia-great-place-live</a>

                        <p>With a population of 31 million people, Malaysia is one of Asia’s most trending Migration
                            destinations. The country is developing at a staggering rate and offers a number of
                            opportunities in <a href="https://www.evs.lk/view-offer/mm2h-visa" target="_blank">all
                                aspects for skilled workers looking to migrate and invest.</a> </p>

                        <p>Plus, Malaysia is renowned for their health care, education, living standards and cultural
                            similarities they share with Sri Lanka.
                        </p>

                        <h4 class="mb-4"><b>4. New Zealand </b></h4>

                        <img src="images/students-blog/newzealand.jpg" alt="" class="middle-img">

                        <p><a href="https://www.theinvisibletourist.com/living-in-wellington/"
                                target="_blank">https://www.theinvisibletourist.com/living-in-wellington/</a>
                        <p>New Zealand has a population of less than 5 million people and has a low unemployment rate as
                            well. As it’s an English speaking country, language isn’t a barrier at all. </p>

                        <p>Additionally, with high quality living standards, education and job opportunities, skilled
                            workers can easily find jobs here.
                        </p>

                        <p>It would take 5 years to <a href="https://www.evs.lk/" target="_blank">be a Kiwi</a>, but
                            once you are, you can easily travel to Australia.</p>

                        <h4 class="mb-4"><b>5. Germany </b></h4>

                        <img src="images/students-blog/germany.jpg" alt="" class="middle-img">

                        <p><a href="https://www.thecrazytourist.com/15-best-places-visit-germany/"
                                target="_blank">https://www.thecrazytourist.com/15-best-places-visit-germany/</a>
                        <p>With its sturdy Economy, Germany is one of Europe’s strongest countries and the best to
                            Migrate if you plan to stay there for quite a long time. However the catch is learning the
                            German language to communicate fluently. </p>
                        <p>Germany is currently looking for skilled workers such as Developers, Engineers, IT
                            Professionals, Health and Social Workers etc.
                        </p>

