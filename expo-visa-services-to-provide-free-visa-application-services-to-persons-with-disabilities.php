                        <h1 class="title">Expo Visa Services to provide free visa application services to persons with disabilities</h1>

                        <p class="first-p">
                        In a bid to extend a practical helping hand to persons with disabilities, Expo Visa Services, a subsidiary of Expolanka Holdings reached out to the Specialised Training and Disability Resource Centre of Employers’ Federation of Ceylon (EFC) to provide visa application services to persons with disabilities seeking foreign training opportunities. Accordingly, Expo Visa Services will provide all visa related services to persons with disabilities through EFC free of charge except for the visa processing fees charged by embassies. 
                        </p>

                        <p>Expo Visa Services will facilitate the entire visa application process from filling applications, documentation, collecting and delivering documents, embassy submission and accompanying applicants to their visa submissions where necessary. The Company will also provide other services such as attestation, translations and more importantly door to door services. </p>

                        <p>“At the moment, visa application is a stressful process to persons with disabilities. Lack of proper guidance is one of the main setbacks they face during the entire process. We are grateful to Expo Visa Services for offering their expert services. Guidance from a professional and dedicated entity will make a world of difference to individuals with disabilities in their attempts to obtain visa and uplift their lifestyles,” commented Specialised Training and Disability Resource Centre Manager Manique.</p>


