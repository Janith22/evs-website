                        <h1 class="title">School’s finished- Planning to Study Overseas ?</h1>

                        <p class="first-p">
                        Are you bombarded with numerous education options and don’t know what to choose? The first challenge is to find the university that meets your needs. This is ‘your’ life. Don’t be influenced by your peers or the most colourful advertisement.                         </p>

                        <p>If your parents are sponsoring you, be grateful. Don’t force them to spend money that they don’t have and feel guilty about it later.</p>
                        
                        <p><strong>The education destination of your choice.</strong></p>

                        <p>One of the first points to consider is the country that you want to go to. Some of the things you should consider are your budget- tuition fees and the cost of living, climate, language, employment opportunities for students while at university and on graduation.</p>

                        <p><strong>Is the study programme right?</strong><p>

                        <p>The selection of the right study programme can make or break your university experience. Be 200% sure that you are satisfied with your selection. Be it a diploma, degree or even post graduate programme, make sure you are happy with the course content.  Do your research. Check job options. You will spend your next three to six years studying it, and the next 40-50 years working in that subject area. This is the time to identify your passions and strengths! Make a career out of it, and your future will be a happier one!</p>
                        
                        <p><strong>Consult university rankings and recognition.</strong></p>

                        <p>If you are planning to work in Sri Lanka, select a university that is well known in the island. Similarly, if Canada is the end goal, make sure the university you select is respected in Canada! Be mindful of education scams. Get advice from reputed education consultants; like EVS, before applying.</p>

                        <p><strong>Find out what the university resources are. </strong></p>

                        <p>The university library is priority. The extra curriculum activities available is another question that you should ask. Sports teams, clubs and society add spice to the university life. University life is not just about studying! Study the university website, and ask questions from your education consultant or the student counsellor at the university.</p>

                        <p><strong>Check out the faculty</strong></p>

                        <p>The education comes from the faculty. Inquire as to who the lecturers for your study program will be. Check what their qualifications are. A simple rule is that a lecturer should have a minimum of one qualification higher than the qualification that is being administered. I.e. to teach a bachelor’s degree, the lecturer must have at least a master’s degree. </p>

                        <p><strong>Select the right agent to start the process</strong></p>

                        <p>While some students opt to apply on their own, having a credible and experienced agent will help you speed up the process, while finding the best deals. Universities offer packages and deals such as free accommodation, air fare, part and full scholarships. Your agent will be able to advise you on how to maximise on such benefits.</p>
                        
